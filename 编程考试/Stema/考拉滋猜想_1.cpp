#include <iostream> // 引入C++的输入/输出库
using namespace std; // 使用标准库的命名空间，这样我们就可以直接使用标准库的名字，而不需要在前面加上std::

int main() { // 程序的主函数，程序的执行从这里开始
    int N; // 声明一个整数变量N，用来存储输入的正整数
    cin >> N; // 从输入读取一个值赋给N

    int steps = 0; // 声明一个整数变量steps，并初始化为0。这个变量用来记录N变成1需要的步数

    while (N != 1) { // 开始一个while循环，只要N不等于1，就会继续循环
        if (N % 2 == 0) { // 检查N是否是偶数
            N /= 2; // 如果N是偶数，就把N除以2
        } else { // 如果N不是偶数
            N = N * 3 + 1; // 就把N乘以3再加1
        }
        steps++; // 把steps的值增加1，表示我们进行了一步操作
    }

    cout << steps << endl; // 输出steps的值，然后输出一个换行符

    return 0; // 表示主函数的结束。返回0表示程序成功执行
}