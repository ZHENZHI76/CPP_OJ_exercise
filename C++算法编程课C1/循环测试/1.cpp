#include <iostream>
using namespace std;

int main() {
    int h, w, f;
    char s;
    cin >> h >> w >> s >> f;
    for (int i = 1; i <= h; i++) {
        for (int j = 1; j <= w; j++) {
            if (f == 1) {
                cout << s;
            } else {
                if (i == 1 || i == h || j == 1 || j == w) { // 可以合并为 if (i == 1 || i == h || j == 1 || j == w || f == 1)
                    cout << s;
                } else {
                    cout << " ";
                }
            }
        }
        cout << endl;
    }
    return 0;
}
/*
这段代码是用于解决画矩形问题的:

1. 输入矩形的高a,宽b,字符c和是否填充f

2. 使用两层嵌套for循环遍历矩形的每一行每一列

3. 如果是矩形边界位置(第一行、最后一行、第一列、最后一列)或者指定填充实心(f=1),则输出字符c

4. 否则输出空格

5. 最终输出一个由字符c组成的空心或实心矩形

6. 通过两个for循环遍历行列,if条件判断控制输出字符或空格

7. 从而实现了根据输入参数绘制指定样式矩形的效果

8. 匹配了题目中输入高宽字符和填充标志,输出矩形图案的要求

综上,这个代码利用嵌套循环和条件判断,根据输入参数绘制了空心或实心的矩形,实现了题目的基本要求。
关键在于遍历每一个位置,判断是否输出字符来控制图案。
*/