#include <iostream> // 引入iostream库，用于输入输出操作
using namespace std; // 使用std命名空间，这样就可以直接使用std库中的函数和对象，而不需要加上std::前缀

int main() {
    int N, M; // 定义两个整数变量N和M，分别表示灯的数量和人的数量
    cin >> N >> M; // 从标准输入读取N和M的值

    // 用一个for循环遍历所有的i，从1开始，直到i的平方大于N为止
    for (int i = 1; i * i <= N; i++) {
        cout << i * i; // 输出i的平方，这是因为我们要找的是完全平方数

        // 如果i的平方小于N，并且(i + 1)的平方也小于等于N，那么就输出一个逗号
        // 这是因为我们需要在每个完全平方数之间输出一个逗号
        if (i * i < N && (i + 1) * (i + 1) <= N) {
            cout << ",";
        }
    }

    cout << endl; // 输出一个换行符，表示输出结束

    return 0; // 主函数返回0，表示程序正常结束
}