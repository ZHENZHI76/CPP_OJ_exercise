int main() {
    int n;
    cin >> n;
    while (n != 1) {
        if (n % 2 == 0) {
            cout << n << "/2=" << n / 2 << endl;
            n /= 2;
        } else {
            cout << n << "*3+1=" << n * 3 + 1 << endl;
            n = n * 3 + 1;
        }
    }
    cout << "End" << endl;
    return 0;
}
/*
这道题是经典的角谷猜想问题，也称为奇偶归一问题。题目要求输入一个正整数，然后按照一定的规则进行处理，最终得到1。规则是：如果输入的数是奇数，则将其乘以3再加1；如果输入的数是偶数，则将其除以2。重复这个过程，直到得到1为止。

我们可以使用 while 循环来实现这个过程。每次循环中，判断当前的数是奇数还是偶数，然后按照规则进行处理。如果是偶数，则将其除以2；如果是奇数，则将其乘以3再加1。最后输出 "End"。
*/